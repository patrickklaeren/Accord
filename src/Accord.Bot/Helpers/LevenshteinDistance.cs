using System;

namespace Accord.Bot.Helpers;

public static class LevenshteinDistance
{
    public static int GetDamerauLevenshteinDistance(this string @this, string target)
    {
        var bounds = new {Height = @this.Length + 1, Width = target.Length + 1};

        int[,] matrix = new int[bounds.Height, bounds.Width];

        for (int height = 0; height < bounds.Height; height++) { matrix[height, 0] = height; }

        for (int width = 0; width < bounds.Width; width++) { matrix[0, width] = width; }

        for (int height = 1; height < bounds.Height; height++)
        {
            for (int width = 1; width < bounds.Width; width++)
            {
                int cost = (@this[height - 1] == target[width - 1]) ? 0 : 1;
                int insertion = matrix[height, width - 1] + 1;
                int deletion = matrix[height - 1, width] + 1;
                int substitution = matrix[height - 1, width - 1] + cost;

                int distance = Math.Min(insertion, Math.Min(deletion, substitution));

                if (height > 1 && width > 1 && @this[height - 1] == target[width - 2] && @this[height - 2] == target[width - 1])
                {
                    distance = Math.Min(distance, matrix[height - 2, width - 2] + cost);
                }

                matrix[height, width] = distance;
            }
        }

        return matrix[bounds.Height - 1, bounds.Width - 1];
    }
}